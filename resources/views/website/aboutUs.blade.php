						<h2>History Of The Scorers’ Association</h2>
						<p align="justify"> In the year 1999, in view of the forthcoming under 19 World Cup Cricket Tournament to be held in Sri Lanka in the year 2000, the first ever formal training program for our senior scorers was conducted by Mr. Ajith C.S. Perera (ACU&S, England), the only qualified training instructor at the time. This seminar was held at the Taj Samudra Hotel over three days. Subsequently the Scorers’ Guild was established in the year 2000; this was the predecessor to the Scorers’ Section of the Association of Cricket Umpires and Scorers. The first Scorers’ Committee was led by senior scorers Thushara Cooray, Asoka Chandrasekera, Chaminda de Silva, A. Francke, Keerthi Ranasuriya, Saman Amarakoon, M. Nandalal, and F.M. Jayawardena. Office space for meetings was provided by the NCC, Cricinfo, and other organisations, whose generous assistance is gratefully acknowledged.Subsequent to discussions with the Association of Cricket Umpires of Sri Lanka in 2001, the Scorers’ Committee became the Scorers’ Section of that body.</p>
